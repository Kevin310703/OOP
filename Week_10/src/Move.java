public class Move {
    private int startX;
    private int endX;
    private int startY;
    private int endY;
    private Piece movedPiece;
    private Piece killedPiece;

    /**
     * Constructor 1.
     * */
    public Move(int startX, int endX, int startY, int endY, Piece movedPiece) {
        this.startX = startX;
        this.endX = endX;
        this.startY = startY;
        this.endY = endY;
        this.movedPiece = movedPiece;
    }

    /**
     * Constructor 2.
     * */
    public Move(int startX, int endX, int startY, int endY, Piece movedPiece, Piece killedPiece) {
        this.startX = startX;
        this.endX = endX;
        this.startY = startY;
        this.endY = endY;
        this.movedPiece = movedPiece;
        this.killedPiece = killedPiece;
    }

    /**
     * Phương thức toString()trả về thông tin đại diện cho object Move.
     * format: Màu-ký kiệu quốc tê + tọa độ di chuyển theo chuẩn Algebraic notation.
     * */
    @Override
    public String toString() {
        String pieceColor = movedPiece.getColor();
        String pieceSymbol = movedPiece.getSymbol();
        String endCoordinate = convertToAlgebraicNotation(endX, endY);
        return pieceColor + "-" + pieceSymbol + endCoordinate;
    }

    /**
     * Hàm chuyển đổi sang chuẩn Algebraic notation.
     * */
    private String convertToAlgebraicNotation(int x, int y) {
        char file = (char) ('a' + x - 1);
        return "" + file + y;
    }

    public int getStartX() {
        return startX;
    }

    public void setStartX(int startX) {
        this.startX = startX;
    }

    public int getEndX() {
        return endX;
    }

    public void setEndX(int endX) {
        this.endX = endX;
    }

    public int getStartY() {
        return startY;
    }

    public void setStartY(int startY) {
        this.startY = startY;
    }

    public int getEndY() {
        return endY;
    }

    public void setEndY(int endY) {
        this.endY = endY;
    }

    public Piece getMovedPiece() {
        return movedPiece;
    }

    public void setMovedPiece(Piece movedPiece) {
        this.movedPiece = movedPiece;
    }

    public Piece getKilledPiece() {
        return killedPiece;
    }

    public void setKilledPiece(Piece killedPiece) {
        this.killedPiece = killedPiece;
    }
}